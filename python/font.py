MATRIX_HEIGHT = 8
MATRIX_WIDTH = 13
CHAR_WIDTH = 5
CHAR_HEIGHT = 8


def text_to_scroll_frames(
    text: str,
    brightness: int = 1,
    step: int = 1,
    char_spacing: int = 1,
) -> list[list[list[int]]]:
    """Build scrolling 8×13 frames for the given text.

    Renders all characters into a wide pixel "tape" and slides a 13-column
    window left across it (text moves right-to-left like a classic LED ticker).
    The window starts with the tape fully off the right edge and ends after
    the tape has fully exited the left edge.

    Args:
        text: String to scroll (will be upper-cased).
        brightness: Value for lit pixels.
        step: Columns advanced per frame (1 = smoothest, 2 = faster, etc.).
        char_spacing: Blank columns inserted between characters.

    Returns:
        List of 8×13 row-major frames, values in {0, brightness}.
    """
    H = MATRIX_HEIGHT   # 8
    W = MATRIX_WIDTH    # 13

    # Build tape as column-major list: tape[col] = list of H pixel values
    blank_col = [0] * H
    tape: list[list[int]] = []

    for char in text.upper():
        bitmap = _FONT.get(char, _UNKNOWN)
        for col_idx in range(CHAR_WIDTH):
            tape.append([bitmap[row][col_idx] * brightness for row in range(H)])
        for _ in range(char_spacing):
            tape.append(blank_col[:])

    # W blank cols before (text starts off-screen right)
    # W blank cols after  (text exits off-screen left)
    tape = [blank_col[:]] * W + tape + [blank_col[:]] * W

    # Slice 13-column windows, advancing by `step` each frame
    frames: list[list[list[int]]] = []
    pos = 0
    while pos + W <= len(tape):
        frame = [[tape[pos + c][r] for c in range(W)] for r in range(H)]
        frames.append(frame)
        pos += step

    return frames


def char_to_frame(char: str, brightness: int = 1) -> list[list[int]]:
    """Return an 8×13 row list for a single character, centered horizontally.

    Args:
        char: The character to render (will be upper-cased).
        brightness: Value used for lit pixels (default 1; caller scales to
            brightness_levels-1 as needed).

    Returns:
        list[list[int]]: 8 rows × 13 columns, values in {0, brightness}.
    """
    c = char.upper()
    bitmap = _FONT.get(c, _UNKNOWN)

    col_offset = (MATRIX_WIDTH - CHAR_WIDTH) // 2  # = 4

    frame: list[list[int]] = []
    for row_bits in bitmap:
        full_row = [0] * MATRIX_WIDTH
        for i, val in enumerate(row_bits):
            if val:
                full_row[col_offset + i] = brightness
        frame.append(full_row)

    return frame
